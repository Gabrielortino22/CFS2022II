//¿A que se llama Herencia?
//herencia en que se pueden crear clases teniendo como base (clase padre) una clase existente , la nueva clase tendra todos los atributos y metodos de la clase padre , ademas se le podran agregar atributos y metodos propios .
//¿Qué es una subclase?
//es una nueva clase que hereda los atributos y metodos de la clase padre.
//Que es un objeto?
//es una instancia de una clase.
//¿Qué significa que un atributo sea protegido?
//protegido:solo lo pueden compartir las clases que la heredan , solo lo ven la clase padre e hija , no las otras instancias (objetos).
//Existe la herencia múltiple en Typescript?
//no existe la herencia multiple en Typescript.
//Definir Poliformismo
//es cuando dos clases que heredan de otra implementan de  manera diferente un  mismo metodo .
//¿Qué es un constructor? Para qué sirve?
//es un metodo dentro de una clase , que permite crear un objeto a partir de los parametros que se le pasen .
//¿Cómo se crea una clase abstracta?
//una clase abstracta se crea cuando se deja sin definir al menos un atributo o metodo .
//¿Para que se usa el super?
//se usa para referenciar las variables de la clase padre .
//¿Qué son las interfaces?
//las interfaces son como contratos , los atributos y metodos estan declarados pero no definidos ; sirven para suplir las herencias multiples .
